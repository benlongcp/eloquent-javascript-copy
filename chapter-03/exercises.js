////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  
  //create count variable
  let count = 0
  //loop throught the string
  for (let i = 0; i < string.length; i++){
    //check if the current index of the string is equal to the input char
    if (string[i] === char){
      count++;
    }
  }
  return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  let bCount = 0
  //iterate through the string
  for (let i = 0; i < string.length; i++){
    //check if the current lowercased string index element is equal to 'b'
    if (string[i].toLowerCase() === 'b'){
      bCount++;
    }
  }
  return bCount;
}

